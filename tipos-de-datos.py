# 2. Texto
# 3. Booleano

#Ejemplo de numero entero
print("----------------------------")
print("Dato numerico Entero:")
numero = 75
print(numero)
print("Su tipo:")
print(type(numero))
print("----------------------------")

#Ejemplo de numero coma flotante
print("Dato numerico Coma Flotante:")
numeroFloat = 100.235
print(numeroFloat)
print("Su tipo:")
print(type(numeroFloat))
print("----------------------------")

#Ejemplo de dato tipo texto
print("Dato  tipo texto:")
texto = "ESTE ES UN DATO DEL TIPO TEXTO!"
print(texto)
print("Su tipo:")
print(type(texto))
print("----------------------------")

#Ejemplo de dato tipo booleano
print("Dato  tipo booleano:")
datoBooleano = True
print(datoBooleano)
print("Su tipo:")
print(type(datoBooleano))
print("----------------------------")

